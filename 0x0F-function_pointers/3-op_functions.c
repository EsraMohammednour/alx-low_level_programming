#include "3-calc.h"

/**
 * op_add - add to numbers
 * @a: variable 1
 * @b: variable 2
 * Return: int value
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub to numbers
 * @a: variable 1
 * @b: variable 2
 * Return: int value
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multipliction to numbers
 * @a: variable 1
 * @b: variable 2
 * Return: int value
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - dividion of two numbers
 * @a: variable 1
 * @b: variable 2
 * Return: int value
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division for two numbers
 * @a: variable 1
 * @b: variable 2
 * Return: int value
 */

int op_mod(int a, int b)
{
	return (a % b);
}
